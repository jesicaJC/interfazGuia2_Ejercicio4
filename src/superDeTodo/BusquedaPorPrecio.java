try{

if(jtMinimo.getText().isEmpty()){
  precioMinimo=0;
}else{
precioMinimo=Double.parseDouble(jtMinimo.getText());
}

if(jtMaximo.getText().isEmpty()){
  precioMaximo=Double.POSITIVE_INFINITY; 
}else{
precioMaximo=Double.parseDouble(jtMaximo.getText());
}
}catch(Exception e){
    System.out.println("Ocurrio un error: "+e.getMessage());
}

        for (Producto pro:MenuPrincipal.listaProductos) {
            if(pro.getPrecio()>=precioMinimo&& pro.getPrecio()<=precioMaximo){
                
                 modelo.addRow(new Object []{ //Agrega una fila pero coo una fila de objetos
                 pro.getCodigo(),
                     pro.getDescripcion(),
                     pro.getPrecio(),
                     pro.getStock(),
                 
                 
                 });
                 
            }
        }        
